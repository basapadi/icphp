<script setup>
import { ref } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '@/stores/auth'

// <CHANGE> Changed from email to username variables
const username = ref('')
const password = ref('')
const rememberMe = ref(false)
const showPassword = ref(false)
const isLoading = ref(false)
const usernameError = ref('')
const passwordError = ref('')
const loginError = ref('')
const router = useRouter()
const auth = useAuthStore()

const handleLogin = async () => {
  // Reset errors
  usernameError.value = ''
  passwordError.value = ''
  loginError.value = ''

  if (!username.value) {
    usernameError.value = 'Username harus diisi'
    return
  }

  if (username.value.length < 3) {
    usernameError.value = 'Username minimal 3 karakter'
    return
  }

  if (!password.value) {
    passwordError.value = 'Password harus diisi'
    return
  }

  if (password.value.length < 8) {
    passwordError.value = 'Password minimal 6 karakter'
    return
  }

  isLoading.value = true

  try {
    const resp = await auth.login(username.value,password.value);
    if (resp) router.push('/');
  } catch (error) {
    loginError.value = error.response?.data?.message
  } finally {
    isLoading.value = false
  }
}
</script>